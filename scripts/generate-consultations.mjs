import { mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const transcriptsDir = path.join(root, "primock57", "transcripts");
const audioDir = path.join(root, "primock57", "output", "mixed_audio_mp3");
const outputPath = path.join(root, "lib", "consultations-data.ts");

function parseTextGrid(filePath, speaker) {
  const content = readFileSync(filePath, "utf8");
  const lines = content.split(/\r?\n/);
  const utterances = [];
  let current = null;

  for (const rawLine of lines) {
    const line = rawLine.trim();
    if (/^intervals \[\d+\]:$/.test(line)) {
      if (current) utterances.push(current);
      current = { speaker, start: null, end: null, text: "" };
      continue;
    }

    if (!current) continue;

    const xmin = line.match(/^xmin = ([\d.]+)/);
    if (xmin) {
      current.start = Number(xmin[1]);
      continue;
    }

    const xmax = line.match(/^xmax = ([\d.]+)/);
    if (xmax) {
      current.end = Number(xmax[1]);
      continue;
    }

    const text = line.match(/^text = "(.*)"$/);
    if (text) {
      current.text = cleanTranscriptText(text[1].replaceAll('""', '"'));
    }
  }

  if (current) utterances.push(current);

  return utterances.filter(
    (utterance) =>
      Number.isFinite(utterance.start) &&
      Number.isFinite(utterance.end) &&
      utterance.text.length > 0,
  );
}

function cleanTranscriptText(text) {
  return text
    .replaceAll("<UNSURE>", "")
    .replaceAll("</UNSURE>", "")
    .replaceAll("<UNIN/>", "")
    .replaceAll("<INAUDIBLE_SPEECH/>", "")
    .replace(/<[^>]+>/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function titleFromId(id) {
  const match = id.match(/^day(\d+)_consultation(\d+)$/);
  if (!match) return id;
  return `Day ${Number(match[1])} Consultation ${Number(match[2])}`;
}

function metadataFromId(id) {
  const match = id.match(/^day(\d+)_consultation(\d+)$/);
  if (!match) throw new Error(`Unexpected consultation id: ${id}`);
  return {
    day: Number(match[1]),
    consultationNumber: Number(match[2]),
  };
}

const doctorFiles = readdirSync(transcriptsDir)
  .filter((file) => file.endsWith("_doctor.TextGrid"))
  .sort();

const consultations = doctorFiles.map((doctorFile) => {
  const id = doctorFile.replace("_doctor.TextGrid", "");
  const patientFile = `${id}_patient.TextGrid`;
  const audioFile = `${id}.mp3`;
  const doctorPath = path.join(transcriptsDir, doctorFile);
  const patientPath = path.join(transcriptsDir, patientFile);
  const audioPath = path.join(audioDir, audioFile);
  const meta = metadataFromId(id);

  const transcript = [
    ...parseTextGrid(doctorPath, "Doctor"),
    ...parseTextGrid(patientPath, "Patient"),
  ]
    .sort((a, b) => a.start - b.start || a.end - b.end)
    .map((utterance, index) => ({
      id: `${id}-${String(index + 1).padStart(3, "0")}`,
      speaker: utterance.speaker,
      start: Number(utterance.start.toFixed(3)),
      end: Number(utterance.end.toFixed(3)),
      text: utterance.text,
    }));

  try {
    readFileSync(audioPath);
  } catch {
    throw new Error(`Missing MP3 for ${id}: ${audioPath}`);
  }

  return {
    id,
    title: titleFromId(id),
    day: meta.day,
    consultationNumber: meta.consultationNumber,
    audioFile,
    transcript,
  };
});

mkdirSync(path.dirname(outputPath), { recursive: true });

const source = `/* This file is generated by scripts/generate-consultations.mjs. */\n\nimport type { Consultation } from "./consultations";\n\nexport const consultations = ${JSON.stringify(
  consultations,
  null,
  2,
)} as const satisfies readonly Consultation[];\n`;

writeFileSync(outputPath, source);

console.log(`Generated ${consultations.length} consultations at ${outputPath}`);
